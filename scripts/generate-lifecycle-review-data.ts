#!/usr/bin/env tsx
/**
 * Reads all lifecycle_review.md files from the ideas directory and outputs
 * rvahacks/src/data/lifecycle-review.ts as a Record<slug, markdown>.
 *
 * Usage: npx tsx generate-lifecycle-review-data.ts
 */

import { readdirSync, readFileSync, writeFileSync, statSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const HACKATHON_ROOT = join(__dirname, "..");
const IDEAS_ROOT = join(HACKATHON_ROOT, "ideas");
const OUT_FILE = join(HACKATHON_ROOT, "..", "rvahacks", "src", "data", "lifecycle-review.ts");

function findLifecycleReviewFiles(): Array<{ slug: string; path: string }> {
  const results: Array<{ slug: string; path: string }> = [];

  for (const pillarDir of readdirSync(IDEAS_ROOT)) {
    if (pillarDir.startsWith("_") || pillarDir.endsWith(".md") || pillarDir.endsWith(".json") || pillarDir.endsWith(".txt")) continue;
    const pillarPath = join(IDEAS_ROOT, pillarDir);
    if (!statSync(pillarPath).isDirectory()) continue;

    for (const ideaDir of readdirSync(pillarPath)) {
      if (ideaDir.startsWith("_")) continue;
      const reviewFile = join(pillarPath, ideaDir, "lifecycle_review.md");
      try {
        statSync(reviewFile);
        results.push({ slug: ideaDir, path: reviewFile });
      } catch {
        // file doesn't exist — skip
      }
    }
  }

  return results.sort((a, b) => a.slug.localeCompare(b.slug));
}

function escapeTemplateLiteral(str: string): string {
  return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

const files = findLifecycleReviewFiles();
console.log(`Found ${files.length} lifecycle_review.md files`);

const entries = files.map(({ slug, path }) => {
  const content = readFileSync(path, "utf-8").trimEnd();
  console.log(`  ${slug}: ${content.split("\n").length} lines`);
  return `  "${slug}": \`${escapeTemplateLiteral(content)}\``;
});

const output = `// Auto-generated by scripts/generate-lifecycle-review-data.ts
// Do not edit manually — re-run the script to regenerate.
// ${files.length} ideas with lifecycle review content.
export const lifecycleReviewBySlug: Record<string, string> = {
${entries.join(",\n\n")}
};
`;

writeFileSync(OUT_FILE, output, "utf-8");
console.log(`\nWrote ${OUT_FILE}`);
console.log(`File size: ${(output.length / 1024).toFixed(1)} KB`);
